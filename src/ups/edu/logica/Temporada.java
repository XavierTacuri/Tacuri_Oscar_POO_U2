package ups.edu.logica;
//Clase nueva creada "Actor
public class Temporada {
	
	private int numTempo;
	private int numCapitulos;
	public Temporada(int numTempo, int numCapitulos) {
		super();
		this.numTempo = numTempo;
		this.numCapitulos = numCapitulos;
	}
	public int getNumTempo() {
		return numTempo;
	}
	public void setNumTempo(int numTempo) {
		this.numTempo = numTempo;
	}
	public int getNumCapitulos() {
		return numCapitulos;
	}
	public void setNumCapitulos(int numCapitulos) {
		this.numCapitulos = numCapitulos;
	}
	
	

}
